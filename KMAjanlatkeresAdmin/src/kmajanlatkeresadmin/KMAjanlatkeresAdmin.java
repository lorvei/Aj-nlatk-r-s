    public void loadAjanlatok() {
        try {
            Connection con = (Connection) DriverManager.getConnection(DBUrl, DBNev, DBJelszo);
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT ajanlatok.*, munkak.munka AS munk FROM ajanlatok JOIN munkak ON munkak.id=ajanlatok.munka_id");

            ((DefaultTableModel) tbAjanlatok.getModel()).setRowCount(0);
            while (rs.next()) {
                ((DefaultTableModel) tbAjanlatok.getModel()).addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nev"),
                    rs.getString("tel"),
                    rs.getString("munk"),
                    rs.getInt("m"),
                    rs.getString("allapot")
                });
            }

        } catch (SQLException ex) {
            Logger.getLogger(KMAjanlatkeresAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    public void loadMunkak() {
        try {
            Connection con = (Connection) DriverManager.getConnection(DBUrl, DBNev, DBJelszo);
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM munkak");

            ((DefaultTableModel) tbMunkak.getModel()).setRowCount(0);
            while (rs.next()) {
                ((DefaultTableModel) tbMunkak.getModel()).addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("munka")
                });
            }

        } catch (SQLException ex) {
            Logger.getLogger(KMAjanlatkeresAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

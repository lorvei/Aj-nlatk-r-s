    private void btnBejelentkezesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBejelentkezesActionPerformed
        String nev = tfNev.getText();
        String jelszo = pfJelszo.getText();

        MessageDigest m;
        try {
            m = MessageDigest.getInstance("MD5");
            m.update(jelszo.getBytes(), 0, jelszo.length());
            jelszo = new BigInteger(1, m.digest()).toString(16);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(KMAjanlatkeresAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Connection con = (Connection) DriverManager.getConnection(DBUrl, DBNev, DBJelszo);
            Statement stmt = (Statement) con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM felhasznalok WHERE felh_nev='" + nev + "'");

            if (rs.next() && jelszo.equals(rs.getString("jelszo"))) {
                menu.setVisible(true);
                ajanlatFrame.setVisible(true);
                bejelentkezesFrame.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Hibás felhasználónév vagy jelszó!", "Hiba!", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(KMAjanlatkeresAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnBejelentkezesActionPerformed

